    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try 
        {
            String RegNum2   =   txtRegistrationNumber2.getText();
            String parts    =   txtNewParts.getText();
            String note2     =   txtSpecialNote2.getText();
            double cost      =   Double.parseDouble(txtServiceCost.getText());
            java.util.Date oDate = ServiceDate1.getDate();        
            DateFormat oDateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String serviceDate = oDateFormat.format(oDate);  
            
            java.util.Date date=new java.util.Date();  
       
            //check whether the selected date is a future date or not
            if(date.compareTo(oDate)<0)
            {
                JOptionPane.showMessageDialog(null, "Choose current date or passed date");
                
            }
            else
            {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/vehicle_repairing_db","root","");
            String insertServiceSql="INSERT INTO service_details(RegistrationNumber,ServiceDate,NewParts,SpecialNotes,TotalServiceCost) VALUES('"+RegNum2+"','"+serviceDate+"','"+parts+"','"+note2+"',"+cost+")";
            Statement insertServiceStmt = conn.createStatement();
            insertServiceStmt.executeUpdate(insertServiceSql);
            JOptionPane.showMessageDialog(null,"Service Succesfully Inserted");
            } 
        }
        catch(NumberFormatException e)
        {
            System.out.println(e);
            JOptionPane.showMessageDialog(null,"Please fill All Required fields and enter correct values");
        }
        catch (SQLException e2)
        {
            JOptionPane.showMessageDialog(null,"Register the vehicle before insert services records");
        }
    }//GEN-LAST:event_btnAddActionPerformed

