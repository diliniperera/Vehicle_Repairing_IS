    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed
        // TODO add your handling code here:
        String pw= new String(txtPassword.getPassword());
        //give only 3 attempts
        while(attempt!=1){
        if(txtUserName.getText().equals("admin") && pw.equals("12345"))
        {
            //JOptionPane.showMessageDialog(null,"Log in succesfull");
            MainMenu menu= new MainMenu();
            menu.setVisible(true);
            menu.getFrame();
            this.dispose();
            break;
        }
        else if(txtUserName.getText().equals("admin") && !"12345".equals(pw))
        {   
            
            JOptionPane.showMessageDialog(null,"Password is Incorrect Re enter the password");
            txtPassword.setText("");
            attempt--;
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Wrong User name & Password");
            txtUserName.setText("");
            txtPassword.setText("");
            attempt--;
        }
        return;
        }
        //if use all atempts system close automatically
        if(attempt==1)
        {
            JOptionPane.showMessageDialog(null,"Access denied");
            this.dispose();
        }
    }//GEN-LAST:event_btnLogInActionPerformed

