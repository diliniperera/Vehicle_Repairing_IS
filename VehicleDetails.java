    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        try 
        {
            String regNum = txtRegistrationNumber3.getText();
            
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/vehicle_repairing_db","root","");
            String viewVehicleSql="SELECT RegistrationNumber,OwnerName,Mileage,LastServiceDate,SpecialNotes From vehicle_details WHERE RegistrationNumber='"+regNum+"'";
            Statement viewVehicleStmt = conn.createStatement();
            ResultSet rs=viewVehicleStmt.executeQuery(viewVehicleSql);
            
            while (rs.next()) 
            {                
                txtOwnerName.setText(rs.getString("OwnerName"));
                txtMileage.setText(rs.getString("Mileage"));
                txtLastServiceDate.setText(rs.getString("LastServiceDate"));
                txtSpecialNote.setText(rs.getString("SpecialNotes"));
            }
        } 
        catch (Exception e) 
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnViewActionPerformed

